def generate_insurance_products():
    """
    - product_data: (상품명, 한줄설명, 카테고리)로 이미 매핑
    - coverage_details: 위 category에 맞춰 base_coverage_details[category] 중 랜덤
    - 월 보험료: 20,000~100,000 (100원 단위)
    """
    all_rows = []
    product_id = 1

    for company, product_list in product_data.items():
        for (pname, pdesc, category) in product_list:
            # coverage_details 랜덤
            details_list = base_coverage_details.get(category, ["기본 보장내용 없음"])
            coverage_text = random.choice(details_list)

            # 월 보험료 (2만~10만, 100원 단위)
            base_premium = random.randint(200, 1000)  # 200..1000
            premium = base_premium * 100              # => 20000..100000
            premium_str = f"{premium:,}"              # 콤마

            row = [
                product_id,
                company,
                pname,
                pdesc,
                category,
                coverage_text,
                premium_str
            ]
            all_rows.append(row)
            product_id += 1

    return all_rows

def save_insurance_products_csv(data, filename="insurance_products.csv"):
    headers = [
        "product_id",
        "company_name",
        "product_name",
        "short_description",
        "product_type",
        "coverage_details",
        "monthly_premium"
    ]
    with open(filename, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(headers)
        writer.writerows(data)


